#Python Comparison Operators
#1 Equal
x = 5
y = 3
print(x == y) # returns False because 5 is not equal to 3
#2 Not equal
z = 5
t = 3
print(z != t) # returns True because 5 is not equal to 3
#3 Greater than
v = 5
s = 3
print(v > s) # returns True because 5 is greater than 3
#4 Less than
q = 5
r = 3
print(q < r) # returns False because 5 is not less than 3
#5 Greater than or equal to
m = 5
n = 3
print(m >= n) # returns True because five is greater, or equal, to 3

